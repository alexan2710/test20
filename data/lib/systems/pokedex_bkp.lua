reverse_pokemons = {}

function capitalizeFirstLetter(word)
	word = string.lower(word)
    return word:gsub("(%l)(%w*)", function(a,b) return string.upper(a)..b end)
end

function sendDexLists(player)	
	if not reverse_pokemons["Arceus"] then
		loadDexInfos()
	end
	local playerDexInfo = {}


	for id, pokemon in ipairs(pokemons_send) do
		local sto = player:getStorageValue(id + BASE_STORAGE_DEX)
		local name = capitalizeFirstLetter(pokemon.nome)
		local lookType = 0
		local monsterType = MonsterType(name)
		if monsterType then
			lookType = monsterType:getOutfit().lookType
		end
		playerDexInfo[id] = {name = name, found = sto == 1, outfit = {type = lookType}}
	end

	sendOpcodeJson(player, "lists", playerDexInfo)
end

function sendUpdateDex(player, id)
	local update = {
		id = id
	}
	sendOpcodeJson(player, "updateList", update)
end

function Player:handlePokedex(buffer)
	local payload = json.decode(buffer)
    local type = payload.type
	if type == "showPoke" then
		if not reverse_pokemons["Arceus"] then
			loadDexInfos()
		end
		local pokeName = capitalizeFirstLetter(payload.pokeName)
		
		local pokemonId = reverse_pokemons[pokeName]
		local storage = pokemonId + BASE_STORAGE_DEX
		
		if self:getStorageValue(storage) ~= 1 then
			return self:popupFYI("Pokemon Desconhecido")
		end

		sendPokedex(self, pokeName)
		return true
	end
	return false
end

function loadDexInfos()
	local time = os.mtime()
	for index, pokemon in ipairs(pokemons_send) do
		local name = capitalizeFirstLetter(pokemon.nome)
		reverse_pokemons[name] = index
	end
	print("Loaded Pokedex in: " .. (os.mtime() - time)/1000 .. " seconds. Total Pokemons Loaded:" .. #pokemons_send)
end