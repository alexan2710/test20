local function sendOpcodeJson(player, action, data)
  local MAX_PACKET_SIZE = 50000
  
  local buffer = json.encode({action = action, data = data})
  local s = {}
  for i = 1, #buffer, MAX_PACKET_SIZE do
      s[#s + 1] = buffer:sub(i, i + MAX_PACKET_SIZE - 1)
  end
  local msg = NetworkMessage()
  if #s == 1 then
      msg:addByte(50)
      msg:addByte(OPCODE)
      msg:addString(s[1])
      msg:sendToPlayer(player)
      return
  end
  msg:addByte(50)
  msg:addByte(OPCODE)
  msg:addString("S" .. s[1])
  msg:sendToPlayer(player)
  for i = 2, #s - 1 do
      msg = NetworkMessage()
      msg:addByte(50)
      msg:addByte(OPCODE)
      msg:addString("P" .. s[i])
      msg:sendToPlayer(player)
  end
  msg = NetworkMessage()
  msg:addByte(50)
  msg:addByte(OPCODE)
  msg:addString("E" .. s[#s])
  msg:sendToPlayer(player)
end

local function sendDexInfo(player, _table)
	informations = {
	  info = _table
	}
	
	sendOpcodeJson(player, "sendInformation", informations)
end

function sendPokedex(player, pokename)
	local table_tosend	    = {}
	local monsterType     	= MonsterType(pokename)
	if not monsterType then return error(pokename ..  " invalid monster") end
	local monsterNumber   	= 1
	local moveList        	= monsterType:getMoveList()
	local monsterLoot     	= monsterType:getLoot()
	local monsterEvo      	= monsterType:getEvolutionList()
	

	for i, num in ipairs(moveList) do
		num.spell = nil
		num.maxCombatValue = nil
		num.isCombatSpell = nil
		num.minCombatValue = nil
		num.cooldown = num.speed/1000
		num.speed = nil
		num.isMelee = num.isMelee == 1 and true or false
		num.isTarget = num.isTarget == 1 and true or false
		num.range = num.range > 0 and true or false
	end

	for i, num in ipairs(monsterLoot) do
        num.name = getItemName(num.itemId)
		num.clientId = ItemType(num.itemId):getClientId()
		num.subType = nil
		num.text = nil
		num.actionId = nil
		num.childLoot = nil
		num.chance = num.chance/10
        if num.chance > 100 then
            num.chance = 100
        end
	end
	
	local data = {
		name				= pokename,
		looktype            = monsterType:getOutfit().lookType,
		level 				= monsterType:minimumLevel(),
		entry				= monsterNumber,
		moves				= moveList,
		loot				= monsterLoot,
		evo					= monsterEvo,
		fly					= monsterType:isFlyable() > 0 and true or false,
		ride				= monsterType:isRideable() > 0 and true or false,
		surf				= monsterType:isSurfable() > 0 and true or false,
		teleport			= monsterType:canTeleport() > 0 and true or false,
		blink				= monsterType:canBlink() > 0 and true or false,
		dig					= monsterType:canDig() > 0 and true or false,
		smash				= monsterType:canSmash() > 0 and true or false,
		cut					= monsterType:canCut() > 0 and true or false,
		light				= monsterType:canLight() > 0 and true or false,
		levitate			= monsterType:canLevitate() > 0 and true or false,
		controlmind			= monsterType:canControlMind() > 0 and true or false,
		specialDMG			= monsterType:moveMagicAttackBase(),
		physicalDMG			= monsterType:moveMagicAttackBase(),
		health				= monsterType:getMaxHealth(),
		speed				= monsterType:getBaseSpeed(),
		physicalDefense		= monsterType:moveMagicDefenseBase(),
	}
	sendDexInfo(player, data)
end

